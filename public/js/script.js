// Contact form functionality
function setupContactForm() {
    const contactForm = document.querySelector('.contact-form-container');
    
    if (contactForm) {
        contactForm.addEventListener('submit', function(e) {
            e.preventDefault(); // Prevent default form submission
            
            // Get form data
            const name = document.getElementById('name').value.trim();
            const email = document.getElementById('email').value.trim();
            const subject = document.getElementById('subject').value.trim();
            const message = document.getElementById('message').value.trim();
            
            // Validate form data
            if (!name || !email || !subject || !message) {
                showFormMessage(getTranslation('form_validation_required'), 'error');
                return;
            }
            
            // Validate email format
            if (!isValidEmail(email)) {
                showFormMessage(getTranslation('form_validation_email'), 'error');
                return;
            }
            
            // Create mailto link
            const mailtoLink = createMailtoLink(name, email, subject, message);
            
            // Get submit button and show loading state
            const submitBtn = contactForm.querySelector('button[type="submit"]');
            const originalText = submitBtn.textContent;
            submitBtn.disabled = true;
            submitBtn.textContent = getTranslation('form_processing') || 'Processing...';
            
            try {
                // Open email client
                window.location.href = mailtoLink;
                
                // Show success message
                showFormMessage(getTranslation('form_success_message'), 'success');
                
                // Clear form after a delay
                setTimeout(() => {
                    contactForm.reset();
                    clearFormMessage();
                    // Reset button state
                    submitBtn.disabled = false;
                    submitBtn.textContent = originalText;
                }, 2000);
                
            } catch (error) {
                // Reset button state on error
                submitBtn.disabled = false;
                submitBtn.textContent = originalText;
                
                // Fallback: show the email details for manual copying
                showEmailFallback(name, email, subject, message);
            }
        });
    }
}

// Create mailto link with form data
function createMailtoLink(name, email, subject, message) {
    const recipient = 'info@aipluseducation.com';
    const mailtoSubject = encodeURIComponent(`Contact Form: ${subject}`);
    
    const mailtoBody = encodeURIComponent(
        `Name: ${name}\n` +
        `Email: ${email}\n` +
        `Subject: ${subject}\n\n` +
        `Message:\n${message}\n\n` +
        `---\n` +
        `This message was sent from the AI Plus Education website contact form.`
    );
    
    return `mailto:${recipient}?subject=${mailtoSubject}&body=${mailtoBody}`;
}

// Email validation
function isValidEmail(email) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
}

// Show form message
function showFormMessage(message, type) {
    // Remove existing message
    clearFormMessage();
    
    const messageDiv = document.createElement('div');
    messageDiv.className = `form-message form-message-${type}`;
    messageDiv.textContent = message;
    
    const form = document.querySelector('.contact-form-container');
    const submitBtn = form.querySelector('button[type="submit"]');
    
    form.insertBefore(messageDiv, submitBtn);
    
    // Auto-remove error messages after 5 seconds
    if (type === 'error') {
        setTimeout(() => {
            clearFormMessage();
        }, 5000);
    }
}

// Clear form message
function clearFormMessage() {
    const existingMessage = document.querySelector('.form-message');
    if (existingMessage) {
        existingMessage.remove();
    }
}

// Fallback for when mailto doesn't work
function showEmailFallback(name, email, subject, message) {
    const fallbackContent = `
        <div class="email-fallback">
            <h3>Please copy this information and send it to:</h3>
            <p><strong>Email:</strong> info@aipluseducation.com</p>
            <p><strong>Subject:</strong> Contact Form: ${subject}</p>
            <div class="email-content">
                <p><strong>Name:</strong> ${name}</p>
                <p><strong>Email:</strong> ${email}</p>
                <p><strong>Subject:</strong> ${subject}</p>
                <p><strong>Message:</strong></p>
                <p>${message}</p>
            </div>
            <button onclick="closeFallback()" class="btn btn-secondary">Close</button>
        </div>
    `;
    
    // Create overlay
    const overlay = document.createElement('div');
    overlay.className = 'email-fallback-overlay';
    overlay.innerHTML = fallbackContent;
    
    document.body.appendChild(overlay);
}

// Close fallback modal
function closeFallback() {
    const overlay = document.querySelector('.email-fallback-overlay');
    if (overlay) {
        overlay.remove();
    }
}

// Mobile navigation functionality
function setupMobileNavigation() {
    const mobileNavToggle = document.getElementById('mobile-nav-toggle');
    const mobileNavMenu = document.getElementById('mobile-nav-menu');
    const mobileLanguageToggle = document.getElementById('mobile-language-toggle');
    const mobileLanguageDropdown = document.getElementById('mobile-language-dropdown');
    const mobileCurrentLang = document.getElementById('mobile-current-lang');
    
    // Mobile navigation toggle
    if (mobileNavToggle && mobileNavMenu) {
        mobileNavToggle.addEventListener('click', function(e) {
            e.stopPropagation();
            mobileNavToggle.classList.toggle('active');
            mobileNavMenu.classList.toggle('active');
        });
        
        // Close mobile menu when clicking on links
        const mobileNavLinks = mobileNavMenu.querySelectorAll('a');
        mobileNavLinks.forEach(link => {
            link.addEventListener('click', function() {
                mobileNavToggle.classList.remove('active');
                mobileNavMenu.classList.remove('active');
            });
        });
        
        // Close mobile menu when clicking outside
        document.addEventListener('click', function(e) {
            if (!mobileNavToggle.contains(e.target) && !mobileNavMenu.contains(e.target)) {
                mobileNavToggle.classList.remove('active');
                mobileNavMenu.classList.remove('active');
            }
        });
    }
    
    // Mobile language selector
    if (mobileLanguageToggle && mobileLanguageDropdown) {
        mobileLanguageToggle.addEventListener('click', function(e) {
            e.stopPropagation();
            mobileLanguageDropdown.classList.toggle('active');
        });
        
        // Mobile language dropdown buttons
        const mobileLanguageButtons = mobileLanguageDropdown.querySelectorAll('button[data-lang]');
        mobileLanguageButtons.forEach(button => {
            button.addEventListener('click', function() {
                const lang = this.getAttribute('data-lang');
                changeLanguage(lang);
                // Update mobile language display
                if (mobileCurrentLang) {
                    mobileCurrentLang.textContent = lang.toUpperCase();
                }
                mobileLanguageDropdown.classList.remove('active');
            });
        });
    }
    
    // Sync language between desktop and mobile
    function syncLanguageDisplay() {
        const currentLang = currentLanguage.toUpperCase();
        if (currentLangSpan) currentLangSpan.textContent = currentLang;
        if (mobileCurrentLang) mobileCurrentLang.textContent = currentLang;
    }
    
    // Override the existing changeLanguage function to sync mobile
    const originalChangeLanguage = changeLanguage;
    changeLanguage = function(lang) {
        originalChangeLanguage(lang);
        syncLanguageDisplay();
    };
    
    // Mobile breakpoint handling
    const mobileBreakpoint = 768;
    
    function checkMobile() {
        if (window.innerWidth <= mobileBreakpoint) {
            document.body.classList.add('mobile');
        } else {
            document.body.classList.remove('mobile');
            // Close mobile menu when switching to desktop
            if (mobileNavToggle) mobileNavToggle.classList.remove('active');
            if (mobileNavMenu) mobileNavMenu.classList.remove('active');
        }
    }
    
    window.addEventListener('resize', checkMobile);
    checkMobile(); // Initial check
}

