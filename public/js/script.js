// Current language state
let currentLanguage = 'en';

// DOM elements
const languageToggle = document.getElementById('language-toggle');
const languageDropdown = document.getElementById('language-dropdown');
const currentLangSpan = document.getElementById('current-lang');

// Initialize the page
document.addEventListener('DOMContentLoaded', function() {
    // Set initial language
    setLanguage(currentLanguage);
    
    // Language dropdown functionality
    if (languageToggle && languageDropdown) {
        languageToggle.addEventListener('click', function(e) {
            e.stopPropagation();
            languageDropdown.classList.toggle('active');
        });
        
        // Language dropdown buttons
        const languageButtons = languageDropdown.querySelectorAll('button[data-lang]');
        languageButtons.forEach(button => {
            button.addEventListener('click', function() {
                const lang = this.getAttribute('data-lang');
                changeLanguage(lang);
            });
        });
    }
    
    // Start Practice buttons
    const startShsatPracticeBtn = document.getElementById('start-shsat-practice');
    const startSatPracticeBtn = document.getElementById('start-sat-practice');
    
    if (startShsatPracticeBtn) {
        startShsatPracticeBtn.addEventListener('click', function() {
            window.location.href = 'login.html';
        });
    }
    
    if (startSatPracticeBtn) {
        startSatPracticeBtn.addEventListener('click', function() {
            window.location.href = 'login.html';
        });
    }
    
    // Close dropdown when clicking outside
    if (languageDropdown) {
        document.addEventListener('click', function() {
            languageDropdown.classList.remove('active');
        });
    }
    
    // Setup various page functionalities
    setupSmoothScrolling();
    setupHeaderScrollEffect();
    setupScrollAnimations();
    setupHeroButtonHandlers();
    setupContactForm();
    setupMobileNavigation();
});

// Language change function
function changeLanguage(lang) {
    if (!languageDropdown || !currentLangSpan) return;
    
    currentLanguage = lang;
    setLanguage(lang);
    languageDropdown.classList.remove('active');
    
    // Update language display
    currentLangSpan.textContent = lang.toUpperCase();
    
    // Store preference in localStorage
    localStorage.setItem('preferredLanguage', lang);
}

// Set language function
function setLanguage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Get translation helper function
function getTranslation(key) {
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
        return translations[currentLanguage][key];
    }
    // Fallback to English if translation not found
    if (translations['en'] && translations['en'][key]) {
        return translations['en'][key];
    }
    return key; // Return the key itself if no translation found
}

// Smooth scrolling setup
function setupSmoothScrolling() {
    const navLinks = document.querySelectorAll('.nav-links a[href^="#"]');
    
    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            const targetSection = document.querySelector(targetId);
            
            if (targetSection) {
                const headerHeight = document.querySelector('.header').offsetHeight;
                const targetPosition = targetSection.offsetTop - headerHeight;
                
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });
}

// Header scroll effect
function setupHeaderScrollEffect() {
    const header = document.querySelector('.header');
    
    window.addEventListener('scroll', function() {
        if (window.scrollY > 100) {
            header.style.background = 'rgba(255, 255, 255, 0.98)';
            header.style.boxShadow = '0 2px 20px rgba(0, 0, 0, 0.1)';
        } else {
            header.style.background = 'rgba(255, 255, 255, 0.95)';
            header.style.boxShadow = 'none';
        }
    });
}

// Scroll animations setup
function setupScrollAnimations() {
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };
    
    const observer = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.animationPlayState = 'running';
                entry.target.classList.add('animate-in');
            }
        });
    }, observerOptions);
    
    // Observe elements for animation
    const animateElements = document.querySelectorAll('.feature-card, .test-card, .stat-item');
    animateElements.forEach(el => {
        observer.observe(el);
    });
}

